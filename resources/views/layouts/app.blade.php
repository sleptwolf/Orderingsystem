        function disable_category() {
            if(document.getElementById("new_category").value.length == 0){
                document.getElementById("select").disabled = false;
                return;
            }else{
                document.getElementById("select").disabled = true;
            }            
        }        

        function addToForm(item_name) {
            //check if the input already exists or not
            if(document.getElementById(item_name)){
                alert('already');
                return;
            }
            //selecting form
            var form = document.getElementById("order_form");
            //new line
            form.appendChild(document.createElement("br"));
            //adding label
            form.appendChild(document.createTextNode(item_name));
            //adding input for quantity of item
            var qty = document.createElement('input');
            qty.type = 'number';
            qty.name = 'qty[]';
            qty.value = 1;
            form.appendChild(qty);
            //adding hidden input for name of item
            var name = document.createElement('input');
            name.type = 'hidden';
            name.name = 'item[]';
            name.id = item_name;
            name.value = item_name;
            form.appendChild(name);            
        }
